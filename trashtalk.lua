local trashtalk_type = menu.add_combo_box("Trashtalk type", "Misc/Misc", { "Off", "Russian", "English", "Trashfacts[RU]" }, 0)


register_callback("player_death", function(event)
    
    local attacker_index = entitylist.get(event:get_int("attacker"), true)
    local died_index = entitylist.get(event:get_int("userid"), true)
    local me = entitylist.get_local_player()
    
    math.randomseed(os.clock()*100000000000)

        if attacker_index == me and died_index ~= me then      
            if trashtalk_type:get() == 1 then    
                engine.execute_client_cmd("say " .. tostring(messagesRU[math.random(0, #messagesRU)]))
            elseif trashtalk_type:get() == 2 then
                engine.execute_client_cmd("say " .. tostring(messagesENG[math.random(0, #messagesENG)]))
            elseif trashtalk_type:get() == 3 then
                engine.execute_client_cmd("say " .. tostring(trashfacts[math.random(0, #trashfacts)]))
            end
        end
end)
